#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	i;

	i = 0;
	if (n == -2147483648)
		return (i = write(1, "-2147483648", 11));
	else
	{
		if (n < 0)
		{
			i += write(1, "-", 1);
			n = -n;
		}
		if (n >= 10)
			i += ft_putnbr(n / 10);
		return (i += ft_putchar(48 + (n % 10)));
	}
}
